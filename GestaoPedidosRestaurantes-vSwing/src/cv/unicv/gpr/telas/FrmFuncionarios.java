    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        // TODO add your handling code here:
        if (jTableFuncionarios.getSelectedRow() != -1) {
            
            Funcionarios f = new Funcionarios();
            FuncionarioDAO funcionario = new FuncionarioDAO();
            
            f.setIdFuncionarios((int) jTableFuncionarios.getValueAt(jTableFuncionarios.getSelectedRow(), 0));
                    
            funcionario.remover(f, this);
            
            lerTable();
        }
        
        if(jTableFuncionarios.getRowCount()==0) btnRemover.setVisible(false);
    }//GEN-LAST:event_btnRemoverActionPerformed

