    public void pesquisarTable(String palavraChave){
        
        DefaultTableModel modelo = (DefaultTableModel) jTableFuncionarios.getModel();
        modelo.setNumRows(0);
        FuncionarioDAO funcinoariodao = new FuncionarioDAO();
        
        for(Funcionarios f: funcinoariodao.pesquisar(palavraChave, this)){
            
            modelo.addRow(new Object[]{
                f.getIdFuncionarios(),
                f.getNome(),
                f.getCargo(),
                f.getDataNasc(),
                f.getSexo(),
                f.getMorada(),
                f.getTelefone(),
                f.getEmail(),
                f.getDataEntrada()
            });
        }
    }
    
