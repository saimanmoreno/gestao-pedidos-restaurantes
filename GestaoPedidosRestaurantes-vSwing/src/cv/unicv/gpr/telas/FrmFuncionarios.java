    public void lerTable(){
        
        DefaultTableModel modelo = (DefaultTableModel) jTableFuncionarios.getModel();
        modelo.setNumRows(0);
        FuncionarioDAO funcionariodao = new FuncionarioDAO();
        
        for(Funcionarios f: funcionariodao.listar(this)){
            
            modelo.addRow(new Object[]{
                f.getIdFuncionarios(),
                f.getNome(),
                f.getCargo(),
                f.getDataNasc(),
                f.getSexo(),
                f.getMorada(),
                f.getTelefone(),
                f.getEmail(),
                f.getDataEntrada()
            });
        }
    }

