            func.setIdFuncionarios((int) jTableFuncionarios.getValueAt(jTableFuncionarios.getSelectedRow(), 0));
            func.setNome((String) jTableFuncionarios.getValueAt(jTableFuncionarios.getSelectedRow(), 1));
            func.setCargo((String) jTableFuncionarios.getValueAt(jTableFuncionarios.getSelectedRow(), 2));
